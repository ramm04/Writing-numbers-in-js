// Сравнение чисел

let x = 10;
let y = 20;

// пишем оператор равенства между переменными
console.log(x === y); // false, так как x и y не равны.
console.log(x === 10); // true

// Оператор неравенства. "!" в js - Э это отрицание.
console.log(x !== y); // true (x не равно y)
console.log(y !== 20); // false

// Сравнение чисел на больше, меньше
console.log(x > y); // false
console.log(x < y); // true

console.log(x >= y); // false
console.log(x <= y); // true

